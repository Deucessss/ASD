public class Rating
{
    private int cateringRating;
    private int photographyRating;
    private int decorationRating;
    private int hallRating;
    public Rating()
    {
        
    }


    public int getCateringRating() {
    	return this.cateringRating;
    }
    
    public void setCateringRating(int cateringRating) {
    	this.cateringRating = cateringRating;
    }
    
    public int getPhotographyRating() {
    	return this.photographyRating;
    }
    
    public void setPhotographyRating(int photographyRating) {
    	this.photographyRating = photographyRating;
    }
    
    public int getDecorationRating() {
    	return this.decorationRating;
    }
    
    public void setDecorationRating(int decorationRating) {
    	this.decorationRating = decorationRating;
    }
    
    public int getHallRating() {
    	return this.hallRating;
    }
    
    public void setHallRating(int hallRating) {
    	this.hallRating = hallRating;
    }
}
